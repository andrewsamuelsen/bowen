import { clerkMiddleware, createRouteMatcher } from "@clerk/nextjs/server";
import { NextResponse } from "next/server";

const isPublicRoute = createRouteMatcher(['/', '/api/webhooks(.*)']);

export default clerkMiddleware(async (auth, req) => {
  const { userId } = await auth();
  const url = new URL(req.url);

  // If user is logged in and tries to access the landing page, redirect to relationships
  if (userId && url.pathname === '/') {
    return NextResponse.redirect(new URL('/relationships', req.url));
  }

  // If it's not a public route and user is not logged in, clerkMiddleware handles it automatically
  // but we can be explicit if we want. By default, Clerk makes all routes private if not handled.
});

export const config = {
  matcher: [
    // Skip Next.js internals and all static files, unless found in search params
    '/((?!_next|[^?]*\.(?:html?|css|js(?!on)|jpe?g|webp|png|gif|svg|ttf|woff2?|ico|csv|docx?|xlsx?|zip|webmanifest)).*)',
    // Always run for API routes
    '/(api|trpc)(.*)',
  ],
};
